// Helper function to get all stations
export const getAllStations = () => {
  const allStations = [];
  tokyoTrainLines.forEach(line => {
    line.stations.forEach(station => {
      if (!allStations.find(s => s.name === station)) {
        allStations.push({
          name: station,
          lines: [line.name]
        });
      } else {
        const existingStation = allStations.find(s => s.name === station);
        if (!existingStation.lines.includes(line.name)) {
          existingStation.lines.push(line.name);
        }
      }
    });
  });
  return allStations.sort((a, b) => a.name.localeCompare(b.name, 'ja'));
};

// Helper function to search stations
export const searchStations = (query) => {
  const allStations = getAllStations();
  return allStations.filter(station => 
    station.name.includes(query) || 
    station.lines.some(line => line.includes(query))
  );
};